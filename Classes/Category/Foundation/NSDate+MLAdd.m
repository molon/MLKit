#define RETURN_INTERVAL(_unit_,_method_)                                                     \
NSDate *fromDate;\
NSDate *toDate;\
NSCalendar *calendar = [NSCalendar currentCalendar];\
[calendar rangeOfUnit:_unit_ startDate:&fromDate interval:NULL forDate:fromDateTime]; \
[calendar rangeOfUnit:_unit_ startDate:&toDate interval:NULL forDate:toDateTime]; \
NSDateComponents *difference = [calendar components:_unit_ fromDate:fromDate toDate:toDate options:kNilOptions]; \
return ABS([difference _method_]);

+ (NSInteger)yearsBetweenDate:(NSDate*)fromDateTime withDate:(NSDate*)toDateTime {
    RETURN_INTERVAL(NSCalendarUnitYear, year);
}

+ (NSInteger)monthsBetweenDate:(NSDate*)fromDateTime withDate:(NSDate*)toDateTime {
    RETURN_INTERVAL(NSCalendarUnitMonth, month);
}

+ (NSInteger)weeksBetweenDate:(NSDate*)fromDateTime withDate:(NSDate*)toDateTime {
    RETURN_INTERVAL(NSCalendarUnitWeekOfYear, weekOfYear);
}

+ (NSInteger)daysBetweenDate:(NSDate*)fromDateTime withDate:(NSDate*)toDateTime {
    RETURN_INTERVAL(NSCalendarUnitDay, day);
}

+ (NSInteger)hoursBetweenDate:(NSDate*)fromDateTime withDate:(NSDate*)toDateTime {
    RETURN_INTERVAL(NSCalendarUnitHour, hour);
}

+ (NSInteger)minutesBetweenDate:(NSDate*)fromDateTime withDate:(NSDate*)toDateTime {
    RETURN_INTERVAL(NSCalendarUnitMinute, minute);
}

+ (NSInteger)secondsBetweenDate:(NSDate*)fromDateTime withDate:(NSDate*)toDateTime {
    RETURN_INTERVAL(NSCalendarUnitSecond, second);
}

